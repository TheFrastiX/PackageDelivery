using System;
using System.Collections.Generic;

namespace DeliveryParcel.Domain.Enums
{
    /// <summary>
    /// Перечисление статусов получателя посылки.
    /// Используется для отслеживания его роли и состояния в процессе доставки.
    /// </summary>
    public enum RecipientStatus
    {
        /// <summary>
        /// Получатель только добавлен в заказ.
        /// Ожидает подтверждения или начала доставки.
        /// </summary>
        Created = 0,

        /// <summary>
        /// Получатель уведомлен о предстоящей доставке.
        /// </summary>
        Notified = 1,

        /// <summary>
        /// Получатель подтвердил готовность принять посылку.
        /// </summary>
        Confirmed = 2,

        /// <summary>
        /// Курьер прибыл к получателю, ожидает получения посылки.
        /// </summary>
        AwaitingDelivery = 3,

        /// <summary>
        /// Посылка успешно передана получателю.
        /// </summary>
        Delivered = 4,

        /// <summary>
        /// Получатель не смог принять посылку (например, не был дома).
        /// Требуется повторная попытка доставки.
        /// </summary>
        DeliveryFailed = 5,

        /// <summary>
        /// Получатель отменил получение посылки.
        /// Возможно возврат отправителю.
        /// </summary>
        Canceled = 6,

        /// <summary>
        /// Получатель не ответил на уведомления, не подтвердил получение.
        /// </summary>
        Unresponsive = 7,

        /// <summary>
        /// Посылка хранится на складе/в пункте выдачи.
        /// Ожидает самовывоза.
        /// </summary>
        ReadyForPickup = 8,

        /// <summary>
        /// Получатель самостоятельно забрал посылку.
        /// </summary>
        PickedUp = 9
    }
}
